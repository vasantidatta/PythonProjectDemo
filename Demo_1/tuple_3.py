

colors = ('white','pink','gray','blue','yellow','orange','red')

print(type(colors))
''' Accessing the tuple by using index values is 
    also called indexing'''
    
print(colors[2])
print(colors[4])

''' Accessing the tuple items by negative indexing :- returns the items from the last'''

print(colors[-2])
print(colors[-4])

''' Slicing :- getting the sub tuple items'''

print(colors[1:5])

'''below statement returns the all items from 2nd from tuple'''
print(colors[2:])

'''below statement returns the all the items before 5 from 
   the tuple'''
print(colors[:5])    

''' below statement returns the all the items 
    starting from 2nd last item'''
    
print(colors[:-2])     

     